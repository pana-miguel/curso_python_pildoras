# Definimos una variable con un valor entero
variable = 42
print(f"El valor es {variable} tipo de dato es {type(variable)}")

# Cambiamos la variable a un valor de tipo cadena (str)
variable = "Juan Perez"
print(f"El valor es {variable} tipo de dato es {type(variable)}")

# Cambiamos la variable a un valor de tipo flotante (float)
variable = 3.14
print(f"El valor es {variable} tipo de dato es {type(variable)}")

# Cambiamos la variable a un valor booleano (bool)
variable = True
print(f"El valor es {variable} tipo de dato es {type(variable)}")

# Cambiamos la variable a una lista
variable = ["Ana", "Luis", "Pedro"]
print(f"El valor es {variable} tipo de dato es {type(variable)}")

# Cambiamos la variable a un diccionario
variable = {"nombre": "Maria", "edad": 30}
print(f"El valor es {variable} tipo de dato es {type(variable)}")

# Cambiamos la variable a un valor booleano (otra vez)
variable = False
print(f"El valor es {variable} tipo de dato es {type(variable)}")
